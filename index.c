void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if (huart->Instance == UART4)
  {
    const char *startTag = "$GNRMC";
    const char *endlTag = "\r\n";
    int index2 = 0;

    char *startPtr = strstr((char *)rxData, startTag);
    if (startPtr == NULL)
      invalidData();
    else
    {
      while (startPtr != NULL)
      {
        index2++;
        char *endlPtr = strstr(startPtr, endlTag);

        if (endlPtr != NULL)
        {
          invalidData();

          size_t lineLength = endlPtr - startPtr;
          char line[lineLength + 1];
          strncpy(line, startPtr, lineLength);
          line[lineLength] = '\0';

          char *buffers[20];

          char *buffer = strtok(line, ",");
          int tokenIndex = 0;
          while (buffer != NULL)
          {
            buffers[tokenIndex++] = buffer;
            buffer = strtok(NULL, ",");
          }
          if (tokenIndex == 12)
          {
            utcTime = atoi(buffers[1]);

            hours = (utcTime / 10000) + 3;
            minutes = (utcTime % 10000) / 100;
            seconds = utcTime % 100;

            latitude = atof(buffers[3]) / 100.0;
            longitude = atof(buffers[5]) / 100.0;

            speed = atof(buffers[7]) * 1.852 / 3600.0;

            date = atoi(buffers[9]);

            day = date / 10000;
            month = (date % 10000) / 100;
            year = date % 100;
          }
          else
            invalidData();
        }
        else
        {
          break;
        }

        startPtr = strstr(endlPtr, startTag);
      }
    }

  }
  memset(rxData, 0, sizeof rxData);
  HAL_UART_Receive_IT(&huart4, rxData, sizeof rxData);
}
